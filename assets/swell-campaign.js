class campaign{
	constructor(campaigns)
	{
		this.campaigns=campaigns
	}
	renderCampaigns()
	{
		this.campaigns.forEach((campaign,index)=>{
			if(campaign.title!=="Refer a friend")
			{
				$(".campaigns-option").append(
				$("<div>").append(
					$("<img>").addClass(`campaign${index+1}`),
					$("<h6>").text(campaign.title),
					$("<p>").text(campaign.reward_text)).addClass("swell-campaign-link").attr(
					{"data-campaign-id": campaign.id,"data-display-mode": "modal" }))
			}
			else
			{
				$(".campaigns-option").append(
				$("<div>").append(
					$("<img>").addClass(`campaign${index+1}`),
					$("<h6>").text(campaign.title),
					$("<p>").text(campaign.reward_text)).click(function(){
					window.location.href='/pages/referral'
				}))
			}
		})
		$(".swell-campaign-btn a").attr("href", "/pages/referral")
	}
	callPopup()
	{
		swellAPI.showPopupByType("EmailCapturePopup");
	}
}